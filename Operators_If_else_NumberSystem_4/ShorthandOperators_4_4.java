package Operators_If_else_NumberSystem_4;

public class ShorthandOperators_4_4 {
    public static void main (String args[]){
        //! Shorthand Operators
        //*   +=, -=, *=, /=, %=

        int a = 5, b = 10;
        System.out.println("a = " + a + " b = " + b);
        a += b;
        System.out.println("a += b: " + a);
        a -= b;
        System.out.println("a -= b: " + a);
        a *= b;
        System.out.println("a *= b: " + a);
        a/=b;
        System.out.println("a /= b: " + a);
        a%=b;
        System.out.println("a %= b: " + a);


    }
}
